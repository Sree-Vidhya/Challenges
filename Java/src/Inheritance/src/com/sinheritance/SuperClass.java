package com.sinheritance;

public class SuperClass {
    private void method1()
    {
        System.out.println("this is method1 from superclass");
    }
    void method2()
    {
        System.out.println("this is method 2 from superclass");
    }
    protected void method3()
    {
        System.out.println("this is method 3 from superclass\"");
    }
    public void method4()
    {
        System.out.println("this is method4 from superclass");
    }
}
